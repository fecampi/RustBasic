pub fn default_function() {
    // Example 1: Integers
    let integer: i32 = 42; // Declaration of a 32-bit signed integer
    let unsigned_integer: u64 = 123; // 64-bit unsigned integer

    println!("Integer: {}", integer);
    println!("Unsigned Integer: {}", unsigned_integer);

    // Example 2: Floating Point
    let floating_point: f64 = 3.14; // Declaration of a 64-bit floating point number

    println!("Floating Point: {}", floating_point);

    // Example 3: Booleans
    let true_boolean: bool = true;
    let false_boolean: bool = false;

    println!("Boolean (True): {}", true_boolean);
    println!("Boolean (False): {}", false_boolean);
}
