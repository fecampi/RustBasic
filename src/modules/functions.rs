

/// Adds two integers.
///
/// # Arguments
///
/// * `a` - The first number to be added.
/// * `b` - The second number to be added.
///
/// # Returns
///
/// The result of the addition.
///
pub fn soma(a: i32, b: i32) -> i32 {
    let resultado = a + b;
    resultado // O último valor expresso em uma função é implicitamente retornado
}
